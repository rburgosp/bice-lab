package cl.bice.lab.dto;

public class DtoValorTransaccion {
	
	private Double montoPago;
	private Double montoUf;
	private Double montoDollar;
	private int idUsurio ;
	
	public Double getMontoPago() {
		return montoPago;
	}
	public void setMontoPago(Double montoPago) {
		this.montoPago = montoPago;
	}
	public Double getMontoUf() {
		return montoUf;
	}
	public void setMontoUf(Double montoUf) {
		this.montoUf = montoUf;
	}
	public Double getMontoDollar() {
		return montoDollar;
	}
	public void setMontoDollar(Double montoDollar) {
		this.montoDollar = montoDollar;
	}
	public int getIdUsurio() {
		return idUsurio;
	}
	public void setIdUsurio(int idUsurio) {
		this.idUsurio = idUsurio;
	}
	
	
	

}
